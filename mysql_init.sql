CREATE VIEW vw_transactions AS
SELECT
    appointment_id AS transaction_id,
    'appointment' AS type,
    customer_id,
    employee_id,
    branch_id,
    service_id AS item_id,
    appointment_date AS transaction_date,
    amount
FROM appointments
WHERE status = 'Completed'
UNION ALL
